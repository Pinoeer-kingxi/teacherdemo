              <h2>板书技能</h2>
              <h3>含义与目的</h3>
              <ul>
                <li>
                  含义
                </li>
                <li>
                  即教师为辅助课堂口语的表达而写在黑板上的文字或其他符号。包括正板书和副板书。副板书往往写在黑板的两侧，是在教学过程中，作为正板书的补充或注脚而随时写在黑板上的文字。
                </li>
                <li>
                  设计目的
                </li>
                <li>
                  ①提示内容，体现内容结构和教学程序。
                </li>
                <li>
                  ②强化记忆，减轻负担。
                </li>
                <li>
                  ③激发兴趣，启发思考。
                </li>
              </ul>
              <h3>书写要求</h3>
              <ul>
                <li>
                  ①课题的位置要居中，课题的字要大一些。
                </li>
                <li>
                  课题的位置要居中，这个居中指的是你整个板书所占面积的中间位置。课题的字要比下面正文的字要大一些。
                </li>
                <li>
                  ②大小标题的序号要合理
                </li>
                <li>
                  一般按照一、;1.;(1）等这样的层级进行标号。如果层级多也可以按照一、;(一) ; 1.;(1）﹔等进行编号，但是一般板书层级不会超过三个，因为如果层级分的太多，一节课也讲不完。
                </li>
                <li>
                  ③执笔的方法
                </li>
                 <li>
                  执笔的方法要求是捏、挡、指实、掌虚，倾全身之力于笔端。
                </li>
                <li>
                  ④字的要求
                </li>
                <li>
                  字的大小要适当同时字的大小要均一一行字要写整齐字要美观大方
                </li>
              </ul>
              <h3>板书类型</h3>
              <ul>
                <li>
                  ①提纲式
                </li>
                 <li>
                  提纲式板书是对一节课的内容按顺序提纲挚领、条理分明、层次清楚、重点突出的归纳出几个要点。
                </li>
                <li>
                  ②表格式
                </li>
                <li>
                  表格式板书适用于对有关概念、生物的特点及实验等进行归类和对比，从而认识其异同和联系。
                </li>
                <li>
                  ③图示式
                </li>
                <li>
                  这种类型的板书通常用作“过程教学”中，以直观的图画，代替抽象的文字，或用文字、数字、线条及关系框图等来表达知识之间的逻辑顺序，将分散的相关知识系统化。
                </li>
                <li>
                  ④综合式
                </li>
                <li>
                  综合式板书常常用于复习课或者是一节习题课的开始。将教学中所涉及的几方面的知识内容，综合地反映在板书里，或将零散孤立的知识“串联”和“并联”起来,形成系统
                </li>
                <li>
                  ⑤计算式和方程式
                </li>
                <li>
                 它是以数字运算或方程式来表述，文字少,逻辑性强,主要应用于讲解计算题。
                </li>
                <li>
                 ⑥流程式板书（递进式板书)
                </li>
                <li>
                 这种类型的板书多用于连续性较强，环环相扣，层层深入的知识点。教师按照知识点延伸的方向书写板书，使其层次清楚，脉络清晰，形成一个连续的具有一定顺序的发展变化过程，这样的板书为流程式板书。
                </li>
                <li>
                 ⑦比较式板书
                </li>
                <li>
                 是指将具有相同或不同的相互关联的知识点,放在一起加以比较，所形成的板书，这种板书可以采用表格和知识网络,图两种形式。
                </li>
              </ul>
              <h3>应用原则</h3>
              <ul>
                <li>
                  ①科学性原则
                </li>
                <li>
                  板书必须遵循汉字的书写规律,做到书写规范、准确。笔顺正确、不写自造简化字。板书内容不能因为疏忽而造成意思混乱或错误。
                </li>
                <li>
                  ②概括性原则
                </li>
                <li>
                  板书的语言要具有高度的概括性，精炼恰当，能化简为繁,将复杂的知识条理化，有助于学生对教学内容的理解与记忆。
                </li>
                <li>
                  ③条理性原则
                </li>
                <li>
                  教学板书要条理清楚，清晰准确的表现教学内容要点以及知识间的内在联系,不能随意而凌乱，否则会直接影响教学效果。
                </li>
                <li>
                  ④启发性原则
                </li>
                <li>
                  板书的内容应有利于调控学生的思维方向，具有一定的启发性。
                </li>
                 <li>
                  ⑤多样性原则
                </li>
                <li>
                  板书的形式应该灵活多样，板书是由文字、线条、符号、图表和色彩等组成的，从内容到形式都应该是丰富多彩的,不能千篇一律。
                </li>
                <li>
                  ⑥计划性和灵活性原则
                </li>
                <li>
                  板书要有周密的计划，但也要有一定的灵活性。
                </li>
              </ul>
            </span>
