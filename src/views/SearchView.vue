              <h2>体态技能</h2>
              <h3>含义</h3>
              <ul>
                <li>
                  所谓体态语言，是指人在交际过程中，用来传递信息、表达感情、表示态度的非言语的特定身体态势。这里的“交际”是广义的交际，它包括交流、沟通、传播等多种含义。这种特定的身体态势既可以支持、修饰或者否定言语行为; 又可以部分地代替言语行为，发挥独立的表达功能; 同时又能表达言语行为难以表达的感情和态度。具体说来，体态语言就是指人们借以身体动作来传递信息、交流感情、表示某种意义的信息系统。在课堂教学中，教师的体态语言应包括两大方面：一方面是指那些动态的有形的体态语言，即头语、脸语、眼语、手语、身姿等，它在传递信息、影响学生方面具有非常重要的意义。另一方面，教师的体态语言还应包括那些静态的、无形的体态语言，即指教师的仪表风度等。教师的仪表风度是教师崇高的思想感情、气质性格、审美情趣、文化素养的外在表现，是一个人德、才、学、识、体等各种素质在社会交往中的综合体现；它直接参与教师的劳动过程，作用于学生的心灵，影响着教育效果，促进着师生之间的感情交流。
                </li>
              </ul>
              <h3>基本特点</h3>
              <ul><li>
                1.控制性
              </li>
                <li>
                在常态的生活中，我们的体态语言具有相当大的随意性。教师在校园里始终处于众多学生视线的聚焦之下，他的一举一动、一颦一笑，无不影响着学生的情绪，给教学带来或积极或消极的影响。因此教师体态语言表现出一定的“控制性”特点。
              </li>
                <li>
                2.选择性
              </li>
                <li>
                从应用效果这一角度，可以将教师体态语言大致划分为积极体态语言、消极体态语言和无意义体态语言三大类。
              </li>
                <li>
                所谓积极体态语言，是指能够支持或修饰口头讲授、强化授课效果、提高教学效率，能够独立表达有关教学内容，有助于学生理解知识的疑点、难点，能激起学习兴趣的体态语言。
              </li>
                <li>
                所谓消极体态语言，是指非但不能强化课堂教学效果，而且还会分散学生注意力、影响其学习的体态语言。
              </li>
                <li>
                所谓无意义体态语言，则纯属教师因某一方面原因而形成的一些习惯性动作，如，有的教师在授课中喜欢把手插在口袋里，或双手撑在讲桌上。
              </li>
                <li>
                3.学科性
              </li>
                <li>
                不同性质的学科，不同的教学内容，需要运用各不相同的体态语言，因此，教师的体态语言又具有较突出的学科性。
              </li>
              </ul>
              <h3>分类</h3>
              <ul>
                <li>
                  依据美国体态语言专家保罗·埃克曼和弗里森关于体态语言的分类理论,结合我国教师体态语言运用方面的特点，我尝试将教师体态语言分为五大类，即符号性体态语言、说明性体态语言、表露性体态语言、习惯性体态语言和调节性体态语言。
                </li>
                <li>
                  1.教师的符号性体态语言，强调的是教师通过具有符号性和通用性的体态语言符号，向学生传递明确信息。
                </li>
                <li>
                  2.教师的说明性体态语言，则是指教师在教育教学活动中，根据实际需要临时设计一些动作来辅助口语表达，说明教学内容。
                </li>
                <li>
                  3.教师的表露性体态语言，主要用来表达情感倾向，或再现教学内容中的特殊感情。
                </li>
                <li>
                 4.教师的习惯性体态语言，是教师在教育教学活动中自发形成的、带有习惯性特点和个人风格的体态语言。
                </li>
                <li>
                 5.教师的调节性体态语言，是教师身体一部分对另一部分的动作，这些动作常常是内心情绪状态的一种不自觉的反应，-般说来，它对教学常起副作用。
                </li>
                <li>
                 除上述五类外，由于教师在教室等空间的位置与学生的空间距离，同样传递出丰富的信息，且具有教育教学上的现实意义。因此，我们把空间语言作为教师体态语言的另一独特构成部分。
                </li>
              </ul>
              <h3>作用</h3>
              <ul>
                <li>
                  关于教师体态语言的作用，英国体态语言学家洛弗和罗德里克曾将其概括为十类。
                </li>
                <li>
                  1．赞赏学生:微笑，肯定地点点头，拍拍学生的背，把手放在学生的肩上或头上。
                </li>
                <li>
                  2.表扬学生:竖起大拇指，拍手。
                </li>
                <li>
                  3.介绍学生的观点:举起学生的作业纸。
                </li>
                <li>
                  4.对学生的行为表示兴趣:同这个或那个同学建立视线接触。
                </li>
                <li>
                  5．便利学生间的相互交流:采取实际行动以成为学生中的一员，适当时候离开学生集体。
                </li>
                <li>
                  6.指导学生:用手，指某样东西，看着某特定区域，使用事先设计好的信号（如，举起手让学生起立)，扳手指数以强调事物的数量方面，向学生指出答案。
                </li>
                <li>
                  7.显示权威:皱眉、凝视、扬眉，否定地摇头，从不适当的行为旁边走开或不去看它。
                </li>
                <li>
                  8．使学生注意力集中于某重点:走向某人或某物，轻拍一样东西，把头向前探，把手前伸指出重点。
                </li>
                <li>
                  9.间接论证:使用图画，用身体动作进行示范，熟练地操作材料和媒介。
                </li>
                <li>
                  10.忽略学生:在应该有所表示时，缺乏非言语的行为。
                </li>
                <li>
                  这种概括虽然全面但过于琐碎，可从以下几个方面来认识教师体态语言在教学过程中的作用。
                </li>
              </ul>
              <h3>运用原则</h3>
              <ul>
                <li>
                  1、丰富课堂信息容量，提高课堂教学效益
                </li>
                <li>
                  2、调控课堂教学气氛，增进师生之间感情
                </li>
                <li>
                  3、调动学生学习积极性，提高学生学习效率
                </li>
                <li>
                  根据课堂教学的基本规律以及体态语言自身的特点和性质，教师在课堂中要能正确运用体态语言，发挥体态语言的积极作用，就必须遵循以下几项原则性要求：
                </li>
                <li>
                  1、辅助性原则
                </li>
                <li>
                  2、准确性原则
                </li>
                <li>
                  3、共意性原则
                </li>
                <li>
                  4、和谐性原则
                </li>
                <li>
                  5、灵活性原则
                </li>
              </ul>
            </span>
